    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if (namaProduk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Produk belum diisi!");
            return;
        }
        String ProdaukName = namaProduk.getText().trim();

        int kode;
        try {
            kode = Integer.parseInt(codeProduk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kode Produk belum diisi!");
            return;
        }

        int stok;
        try {
            stok = Integer.parseInt(stokProduk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stok Produk belum diisi!");
            return;
        }

        long harga;
        try {
            harga = Long.parseLong(hargaProduk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga Produk belum diisi!");
            return;
        }
        System.out.println(ProdaukName + kode + stok + harga);
        tambah.insertProduk(kode, ProdaukName, stok, harga);
        reftabelProduk();

    }//GEN-LAST:event_jToggleButton1ActionPerformed

