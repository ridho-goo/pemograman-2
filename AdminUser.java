    private void UPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UPDATEActionPerformed
        // TODO add your handling code here:
        if (editNamaProduk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Produk belum diisi!");
            return;
        }
        String ProdaukName = editNamaProduk.getText().trim();

        int kode;
        try {
            kode = Integer.parseInt(editKodeProduk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kode Produk belum diisi!");
            return;
        }

        int stok;
        try {
            stok = Integer.parseInt(editStok.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stok Produk belum diisi!");
            return;
        }

        long harga;
        try {
            harga = Long.parseLong(editHarga.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga Produk belum diisi!");
            return;
        }

        rubah.updateProduk(kode, ProdaukName, stok, harga);
        reftabelProduk();
    }//GEN-LAST:event_UPDATEActionPerformed

