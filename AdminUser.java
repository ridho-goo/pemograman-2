    private void reftabelProduk() {
        try {
            String sql = "SELECT "
                    + "id AS 'ID', "
                    + "produkbarang AS 'Produk Barang', "
                    + "stok AS 'Stok', "
                    + "harga AS 'Harga' "
                    + "FROM produktoko";   // ganti dengan nama tabel Anda

            PreparedStatement pst = cont.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            readTabel.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void refTabelTransaksi() {
        try {
            String sql = "SELECT "
                    + "p.produkbarang AS 'Nama Produk', "
                    + "t.jumlah AS 'Jumlah Produk', "
                    + "t.totalharga AS 'Total Harga', "
                    + "t.tanggal AS 'Tanggal' "
                    + "FROM transaksi t "
                    + "JOIN produktoko p ON t.id_produk = p.id "
                    + "ORDER BY t.tanggal DESC";

            PreparedStatement pst = cont.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            Transaksi.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

